const chartConfig = {
  words: {
    label: "Số từ",
    color: "var(--chart-2)",
  },
} satisfies ChartConfig

export function WordCountChart() {
  const [range, setRange] = React.useState<'all' | 'week' | 'month' | '3month'>('all')

  // Filter chartData by range
  const filteredData = React.useMemo(() => {
    if (range === 'all') return chartData
    const now = new Date()
    let from: Date
    if (range === 'week') from = new Date(now.getFullYear(), now.getMonth(), now.getDate() - 6)
    else if (range === 'month') from = new Date(now.getFullYear(), now.getMonth(), now.getDate() - 29)
    else if (range === '3month') from = new Date(now.getFullYear(), now.getMonth() - 2, now.getDate())
    else from = new Date(0)
    return chartData.filter(d => new Date(d.date) >= from)
  }, [range])

  const total = React.useMemo(
    () => filteredData.reduce((acc, curr) => acc + curr.desktop + curr.mobile, 0),
    [filteredData]
  )

  // Merge desktop + mobile for each day
  const mergedData = filteredData.map(d => ({ date: d.date, words: d.desktop + d.mobile }))

  return (
    <Card className="py-0">
      <CardHeader className="flex flex-col items-stretch border-b !p-0 sm:flex-row space-y-0">
        <div className="flex flex-1 flex-col justify-center gap-1 px-6 pt-4 pb-3 sm:!py-0">
          <CardTitle>Lịch viết</CardTitle>
          <CardDescription>
            Hiển thị số từ viết mỗi ngày
          </CardDescription>
        </div>
        <div className="flex">
          {['all', 'week', 'month', '3month'].map((key) => (
            <button
              key={key}
              data-active={range === key}
              className="data-[active=true]:bg-muted/50 relative z-30 flex flex-1 flex-col justify-center gap-1 border-t px-4 py-2 text-left even:border-l sm:border-t-0 sm:border-l sm:px-6 sm:py-4 text-xs"
              onClick={() => setRange(key as any)}
            >
              <span className="text-nowrap block">
                {key === 'all' && 'Toàn bộ'}
                {key === 'week' && '1 tuần'}
                {key === 'month' && '1 tháng'}
                {key === '3month' && '3 tháng'}
              </span>
            </button>
          ))}
        </div>
        <div className="flex">
          <div className="flex flex-col justify-center gap-1 px-6 py-4">
            <span className="text-muted-foreground text-xs">Tổng số từ</span>
            <span className="text-lg leading-none font-bold sm:text-3xl">{total.toLocaleString()}</span>
          </div>
        </div>
      </CardHeader>
      <CardContent className="px-2 sm:p-6">
        <ChartContainer
          config={chartConfig}
          className="aspect-auto h-[250px] w-full"
        >
          <BarChart
            accessibilityLayer
            data={mergedData}
            margin={{
              left: 12,
              right: 12,
            }}
          >
            <CartesianGrid vertical={false} />
            <XAxis
              dataKey="date"
              tickLine={false}
              axisLine={false}
              tickMargin={8}
              minTickGap={32}
              tickFormatter={(value) => {
                const date = new Date(value)
                return date.toLocaleDateString("vi-VN", {
                  month: "short",
                  day: "numeric",
                })
              }}
            />
            <ChartTooltip
              content={
                <ChartTooltipContent
                  className="w-[150px] bg-white"
                  nameKey="words"
                  labelFormatter={(value) => {
                    return new Date(value).toLocaleDateString("vi-VN", {
                      month: "short",
                      day: "numeric",
                      year: "numeric",
                    })
                  }}
                />
              }
            />
            <Bar dataKey="words" fill={`var(--color-words)`} />
          </BarChart>
        </ChartContainer>
      </CardContent>
    </Card>
  )
}
